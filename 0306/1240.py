import sys
sys.stdin = open('input.txt', 'r')

def scanner(arr, N, M):
    for r in range(N):
        if '1' in arr[r]:
            sr = r
    for c in range(M-1, -1, -1):
        if arr[sr][c] == '1':
            return sr, c-55
            
def solve_code(code):
    
    password = {
        '0001101' : 0,
        '0011001' : 1, 
        '0010011' : 2,
        '0111101' : 3,
        '0100011' : 4,
        '0110001' : 5,
        '0101111' : 6,
        '0111011' : 7,
        '0110111' : 8,
        '0001011' : 9
    }

    number = []
    for i in range(8):
        number.append(password[code[i*7:(i+1)*7]])
    
    check_number = 0
    for i in range(8):
        if i%2 == 0:
            check_number += number[i] * 3
        else:
            check_number += number[i]
    
    if check_number % 10 != 0:
        return 0
    else:
        return sum(number)

T = int(input())

for tc in range(1, T+1):
    N, M = map(int, input().split())
    arr = [list(input()) for _ in range(N)]

    sr, sc = scanner(arr, N, M)
    code = ''
    for i in range(56):
        code += arr[sr][sc+i]

    print(f'#{tc} {solve_code(code)}')
