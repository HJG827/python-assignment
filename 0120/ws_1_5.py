print(3*2)
print(3**2)
# / 은 그냥 나누기=> 계산 결과가 항상 실수, // 은 정수 몫
print((3**2)//(3*2))
# %는 나머지를 구하는 함수
print((3**2)%(3*2))
# 중복연산을 할 때 괄호를 헷갈리지 말 것
print((3**2)+(-3)**2)

number = 3
print(number*2)
print(number**2)
print((number**2)//(number*2))
print((number**2)%(number*2))
print((number**2)+(-number)**2)

